    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String examid = txtexamid.getText();
        String courseid = txtsubject.getText();
        String examdate = txtexamdate.getText();
        String examtime = txtexamtime.getText();
        String semester= txtsemester.getSelectedItem().toString();
        String department= txtdepartment.getSelectedItem().toString();
      
         try {
             pst= conn.prepareStatement("insert into exam(examid,courseid,examdate,examtime,semester,department)values(?,?,?,?,?,?)");
             
             pst.setString(1,examid);
                pst.setString(2,courseid);
                pst.setString(3,examdate);
                pst.setString(4,examtime);
                pst.setString(5,semester);
                pst.setString(6,department);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null,"Examination details added");
                txtexamid.setText("");
                txtsubject.setText("");
                txtexamdate.setText("");
                txtexamtime.setText("");
                txtsemester.setSelectedIndex(-1);
                txtdepartment.setSelectedIndex(-1);
                
                txtexamid.requestFocus();
                
                
                
               
         } catch (SQLException ex) {
             Logger.getLogger(Exam.class.getName()).log(Level.SEVERE, null, ex);
         }
                
               
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


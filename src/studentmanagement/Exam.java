     public void Exam_Load(){
        
        int c;
        try {
            pst= conn.prepareStatement("select * from exam");
              rs= pst.executeQuery();
     
        ResultSetMetaData rsd= rs.getMetaData();
        c= rsd.getColumnCount();
        
        d= (DefaultTableModel)examtable.getModel();
        d.setRowCount(0);
        
        while(rs.next()){
            Vector v2= new Vector();
            for(int i=1;i<=c;i++){
                
                v2.add(rs.getString("examid"));
                v2.add(rs.getString("courseid"));
                v2.add(rs.getString("examdate"));
                v2.add(rs.getString("examtime"));
                v2.add(rs.getString("semester"));
                v2.add(rs.getString("department"));
            }
            d.addRow(v2);
            
        }
        
        
        
        
        } catch (SQLException ex) {
            Logger.getLogger(AdminHomePage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
     }

